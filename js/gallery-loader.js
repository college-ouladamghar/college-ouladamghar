(function () {
  const isInner = window.location.pathname.includes('/pages/');
  const base    = isInner ? '../' : './';

  // ── Build a single gallery item ──────────
  function buildItem(item, lang, opts) {
    const { span2 = false, reveal = true, clickable = true } = opts || {};
    const alt     = item['alt_'     + lang] || item.alt_ar     || '';
    const caption = item['caption_' + lang] || item.caption_ar || '';
    const imgSrc  = base + item.file;

    const classes = [
      'gallery-item',
      span2  ? 'span-2' : '',
      reveal ? 'reveal' : '',
    ].filter(Boolean).join(' ');

    const safeAlt     = alt.replace(/'/g, "\\'");
    const safeCaption = caption.replace(/'/g, "\\'");
    const clickAttr   = clickable
      ? `onclick="window.galleryOpen('${imgSrc}','${item.placeholder}','${safeAlt}','${safeCaption}')"`
      : '';

    const tagBadges = (item.tags || [])
      .filter(t => t !== 'featured')
      .map(t => `<span class="gallery-tag-badge" data-tag="${t}">${t}</span>`)
      .join('');

    return `
      <div class="${classes}" data-id="${item.id}" data-tags="${(item.tags||[]).join(',')}" ${clickAttr}>
        <div class="gallery-item-media">
          <img
            src="${imgSrc}"
            alt="${alt}"
            loading="lazy"
            onerror="this.style.display='none';this.nextElementSibling.style.display='flex';"
          />
          <div class="gallery-placeholder-emoji" style="display:none">${item.placeholder || '🖼️'}</div>
        </div>
        <div class="gallery-item-overlay">
          <div class="gallery-item-info">
            <p class="gallery-item-caption">${caption}</p>
            <div class="gallery-item-tags">${tagBadges}</div>
          </div>
          <span class="gallery-zoom-icon">⤢</span>
        </div>
      </div>`;
  }

  // ── Render full gallery page ─────────────
  function renderFullGallery(container) {
    if (!container) return;
    const items = window.galleryData;
    const lang  = window.i18n ? window.i18n.getLang() : 'ar';

    if (!items.length) {
      container.innerHTML = '<p class="gallery-empty">لا توجد صور بعد.</p>';
      return;
    }

    container.innerHTML = items.map((item, i) =>
      buildItem(item, lang, { span2: i === 0 || i === 6, reveal: true })
    ).join('');

    triggerReveal(container);
  }

  // ── Render home teaser (featured only, max 5) ──
  function renderHomeMosaic(container) {
    if (!container) return;
    const items    = window.galleryData;
    const lang     = window.i18n ? window.i18n.getLang() : 'ar';
    const featured = items.filter(i => (i.tags || []).includes('featured')).slice(0, 5);

    if (!featured.length) {
      container.innerHTML = `
        <div class="gallery-placeholder-grid">
          <div class="gp gp1"></div><div class="gp gp2"></div>
          <div class="gp gp3"></div><div class="gp gp4"></div>
          <div class="gp gp5"></div>
        </div>`;
      return;
    }

    container.innerHTML = `<div class="gallery-mosaic-grid">
      ${featured.map((item, i) =>
        buildItem(item, lang, { span2: i === 0, reveal: false, clickable: false })
      ).join('')}
    </div>`;
  }

  // ── Filter logic ─────────────────────────
  window.galleryFilter = function (tag) {
    const container = document.getElementById('galleryGrid');
    if (!container) return;

    document.querySelectorAll('.filter-btn').forEach(b => {
      b.classList.toggle('active', b.dataset.filter === tag || (!tag && b.dataset.filter === 'all'));
    });

    const items    = window.galleryData;
    const lang     = window.i18n ? window.i18n.getLang() : 'ar';
    const filtered = tag && tag !== 'all'
      ? items.filter(i => (i.tags || []).includes(tag))
      : items;

    container.style.opacity   = '0';
    container.style.transform = 'translateY(8px)';

    setTimeout(function () {
      container.innerHTML = filtered.map((item, i) =>
        buildItem(item, lang, { span2: i === 0, reveal: false })
      ).join('');
      container.style.transition = 'opacity .3s ease, transform .3s ease';
      container.style.opacity    = '1';
      container.style.transform  = '';
    }, 180);
  };

  // ── Lightbox ─────────────────────────────
  window.galleryOpen = function (src, emoji, alt, caption) {
    const lb = document.getElementById('galleryLightbox');
    if (!lb) return;
    const lbImg     = lb.querySelector('.lb-img');
    const lbEmoji   = lb.querySelector('.lb-emoji');
    const lbCaption = lb.querySelector('.lb-caption');

    lbImg.src = src;
    lbImg.alt = alt;
    lbImg.style.display = 'block';
    lbEmoji.style.display = 'none';
    lbImg.onerror = function () {
      lbImg.style.display  = 'none';
      lbEmoji.style.display = 'flex';
      lbEmoji.textContent   = emoji;
    };
    if (lbCaption) lbCaption.textContent = caption;

    lb.classList.add('open');
    document.body.style.overflow = 'hidden';
  };

  // ── IntersectionObserver for reveal ──────
  function triggerReveal(container) {
    const els = container.querySelectorAll('.reveal');
    if (!window.IntersectionObserver) {
      els.forEach(el => el.classList.add('visible'));
      return;
    }
    const obs = new IntersectionObserver(function (entries) {
      entries.forEach(function (e) {
        if (e.isIntersecting) { e.target.classList.add('visible'); obs.unobserve(e.target); }
      });
    }, { threshold: 0.08 });
    els.forEach(el => obs.observe(el));
  }

  // ── Boot ─────────────────────────────────
  const fullGrid   = document.getElementById('galleryGrid');
  const homeMosaic = document.getElementById('galleryMosaic');

  if (fullGrid)   renderFullGallery(fullGrid);
  if (homeMosaic) renderHomeMosaic(homeMosaic);

  // Re-render on language change
  document.addEventListener('langChanged', function () {
    if (fullGrid)   renderFullGallery(fullGrid);
    if (homeMosaic) renderHomeMosaic(homeMosaic);
  });
})();
